class Dataset_Miss(Dataset):
    def __init__(self, root_path, flag='train', size=None, enc_in = 7,use_miss = False, # use_miss代表额外使用miss等特征进行训练
                 features='M', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h', cycle=None):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq
        self.cycle = cycle

        self.use_miss = use_miss
        self.enc_in = enc_in

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))
        '''
        df_raw.columns: ['date','t_idx', [init features], [features_miss], [mask], [t_dif]]
        需要指定参数enc_in
        '''
        date = df_raw['date']
        t_idx = df_raw['t_idx']

        init_features_columns = df_raw.iloc[..., 2:2 + self.enc_in].columns
        cols = list(init_features_columns)
        cols.remove(self.target)

        df_raw_init = df_raw[['date'] + cols + [self.target]]
        df_raw_miss = df_raw[['date'] + [col + '_miss' for col in cols] + [self.target + '_miss']]
        df_raw_mask = df_raw[['mask_' + col for col in cols] + ['mask_' + self.target]]
        df_raw_t_dif = df_raw[['t_dif_' + col for col in cols] + ['t_dif_' + self.target]]


        # print("init_features shape:", init_features.shape)
        # print("features_miss shape:", features_miss.shape)
        # print("mask shape:", mask.shape)
        # print("t_dif shape:", t_dif.shape)
        # print("df_raw shape:", df_raw.shape)

        num_train = int(len(df_raw) * 0.7)
        num_test = int(len(df_raw) * 0.2)
        num_vali = int(len(df_raw) - num_train - num_test)

        # 这里为了更快地调试，先取10%的数据
        only_10 = False
        if only_10 == True:
            num_train = int(num_train // 10)
            num_test = int(num_test // 10)
            num_vali = int(num_vali // 10)

        border1s = [0, num_train - self.seq_len, len(df_raw) - num_test - self.seq_len]
        border2s = [num_train, num_train + num_vali, len(df_raw)]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data_init = df_raw_init.columns[1:]
            cols_data_miss = df_raw_miss.columns[1:]
            df_data_init = df_raw_init[cols_data_init]
            df_data_miss = df_raw_miss[cols_data_miss]
        elif self.features == 'S':
            df_data_init = df_raw_init[[self.target]]
            df_data_miss = df_raw_miss[[self.target + '_miss']]

        if self.scale:
            train_data_miss = df_data_miss[border1s[0]:border2s[0]]
            self.scaler.fit(train_data_miss.values)
            # print(self.scaler.mean_)
            # exit()
            data_miss = self.scaler.transform(df_data_miss.values)
            data_init = self.scaler.transform(df_data_init.values)
        else:
            data_miss = df_data_miss.values
            data_init = df_data_init.values

        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], 1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0)

        # 注意这里的data_x要从miss里取，data_y要从init里取。因为模型输入是miss特征，输出要与init特征算loss
        self.data_x = data_miss[border1:border2]
        self.data_y = data_init[border1:border2]
        self.data_stamp = data_stamp
        self.mask = df_raw_mask[border1:border2]
        self.t_dif = df_raw_t_dif[border1:border2]
        self.t_idx = df_raw['t_idx'][border1:border2]

        # add cycle
        if self.cycle == 0 :
            self.cycle_index = np.zeros(len(data_miss))[border1:border2]
        else:
            self.cycle_index = (np.arange(len(data_miss)) % self.cycle)[border1:border2]

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        # 拼接 data_x 的最后 label_len 部分 和 data_y 的 pred_len 部分
        seq_y = np.concatenate([
            self.data_x[r_begin:s_end],      # 长度 = label_len
            self.data_y[s_end:r_end]         # 长度 = pred_len
        ], axis=0)

        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]
        mask_x = self.mask[s_begin:s_end]
        t_dif_x = self.t_dif[s_begin:s_end]
        t_idx = self.t_idx[s_begin:s_end]

        cycle_index = torch.tensor(self.cycle_index[s_end])

        if self.use_miss == True:  # 如果使用缺失特征，则拼接seq_x和mask_x、t_dif_x等特征
            seq_x = np.concatenate([seq_x, mask_x, t_dif_x, t_idx.to_numpy().reshape(-1,1)], axis=-1)
        # print(f"seq_x的形状是{seq_x.shape}")
        return seq_x, seq_y, seq_x_mark, seq_y_mark, cycle_index

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)

